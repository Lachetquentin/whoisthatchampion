export const getDailyChampion = (date: Date) => {
  const dailyFound = dailies.find((e: any) => {
    const dailyDate = new Date(e.date);
    const isSameDay =
      dailyDate.getDate() -
        date.getDate() +
        dailyDate.getFullYear() -
        date.getFullYear() +
        dailyDate.getMonth() -
        date.getMonth() ===
      0;
    return isSameDay;
  });
  return dailyFound;
};
